package Project;

public class Rectangle2 
{
	public int x1;
	public int y1;
	public int x2;
	public int y2;
	public int x3;
	public int y3;
	public int x4;
	public int y4;
	public int side1;
	public int side2;
	
	public Rectangle2()
	{
		x1 = 0;
		y1 = 1;
		x2 = 0;
		y2 = 1;
		x3 = 0;
		y3 = 1;
		x4 = 0;
		y4 = 1;
	}
	
	public Rectangle2(int a, int b, int c, int d, int e, int f, int g, int h)
	{
		x1 = a;
		y1 = b;
		x2 = c;
		y2 = d;
		x3 = e;
		y3 = f;
		x4 = g;
		y4 = h;
	}
}
